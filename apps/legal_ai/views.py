def _get_citations(jurisdiction):
    base = [{"ref":"ISO/IEC 27037:2012","topic":"Digital Evidence Handling"},{"ref":"NIST SP 800-86","topic":"Forensic Techniques"}]
    extras = {"US":[{"ref":"Fed.R.Evid. 901","topic":"Authentication"},{"ref":"18 U.S.C. 1030","topic":"Computer Fraud"}],
              "EU":[{"ref":"eIDAS Regulation","topic":"Electronic Signatures"},{"ref":"GDPR Art.33","topic":"Breach Notification"}],
              "UK":[{"ref":"PACE Act 1984","topic":"Evidence Admissibility"},{"ref":"Computer Misuse Act 1990","topic":"Cybercrime"}]}
    return base + extras.get(jurisdiction, [])
