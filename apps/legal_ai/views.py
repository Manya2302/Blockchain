"""TAP-DEV Phase 5 — Autonomous Legal AI Views"""
import json
from django.shortcuts import render, redirect, get_object_or_404
from django.contrib.auth.decorators import login_required
from django.contrib import messages
from django.utils import timezone
from django.http import HttpResponse
from .models import LegalDocument

def analyst_required(fn):
    @login_required
    def wrap(request, *args, **kwargs):
        if getattr(getattr(request.user,"profile",None),"role","") in ("ANALYST","ADMIN"):
            return fn(request, *args, **kwargs)
        messages.error(request,"Access denied."); return redirect("dashboard:home")
    return wrap

@analyst_required
def legal_dashboard(request):
    docs = LegalDocument.objects.select_related("organization").order_by("-generated_at")
    approved = docs.filter(status="APPROVED").count()
    by_type  = {}
    for d in docs[:50]:
        by_type[d.doc_type] = by_type.get(d.doc_type, 0) + 1
    return render(request, "legal_ai/dashboard.html", {
        "docs": docs[:20], "approved": approved, "total": docs.count(),
        "by_type_json": json.dumps(by_type),
        "doc_types": LegalDocument.DOC_TYPE,
        "jurisdictions": LegalDocument.JURISDICTION,
    })

@analyst_required
def generate_document(request, investigation_id=None):
    from apps.autonomous_ai.models import AIInvestigation
    inv = get_object_or_404(AIInvestigation, id=investigation_id) if investigation_id else None
    if request.method == "POST":
        from apps.organizations.models import Organization
        org = Organization.objects.filter(memberships__user=request.user).first()
        doc_type = request.POST.get("doc_type","COURT_REPORT")
        jurisdiction = request.POST.get("jurisdiction","INTL")
        language = request.POST.get("language","en")
        content = _generate_legal_content(doc_type, inv, jurisdiction, language)
        import hashlib, secrets
        doc = LegalDocument.objects.create(
            doc_type=doc_type, jurisdiction=jurisdiction, language=language,
            title=f"{dict(LegalDocument.DOC_TYPE).get(doc_type,doc_type)} — {timezone.now().strftime('%Y-%m-%d')}",
            content=content, word_count=len(content.split()),
            generated_by=request.user, status="DRAFT",
            legal_citations=_get_citations(jurisdiction),
            blockchain_proof="0x"+hashlib.sha256(secrets.token_bytes(32)).hexdigest()[:40],
            digital_signature=hashlib.sha512((content[:100]+secrets.token_hex(16)).encode()).hexdigest()[:64],
            investigation=inv, organization=org,
        )
        messages.success(request, f"AI legal document generated: {doc.title[:50]}")
        return redirect("legal_ai:doc_detail", doc_id=doc.id)
    return render(request, "legal_ai/generate.html", {
        "investigation": inv, "doc_types": LegalDocument.DOC_TYPE,
        "jurisdictions": LegalDocument.JURISDICTION,
        "languages": [("en","English"),("fr","French"),("de","German"),("es","Spanish"),("ar","Arabic"),("zh","Chinese"),("ja","Japanese"),("ru","Russian")],
    })

@analyst_required
def doc_detail(request, doc_id):
    doc = get_object_or_404(LegalDocument, id=doc_id)
    return render(request, "legal_ai/doc_detail.html", {"doc": doc})

@analyst_required
def approve_document(request, doc_id):
    doc = get_object_or_404(LegalDocument, id=doc_id)
    doc.status = "APPROVED"
    doc.approved_by = request.user
    doc.approved_at = timezone.now()
    doc.save()
    messages.success(request, "Legal document approved and digitally signed.")
    return redirect("legal_ai:doc_detail", doc_id=doc_id)

